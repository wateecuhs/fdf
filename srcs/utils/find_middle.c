#include "fdf.h"

t_map_elem	*find_center(t_map_elem ***map)
{
	int	i;
	int	j;

	i = 0;
	while (map[i])
		i++;
	j = 0;
	while (map[0][j])
		j++;
	return (map[(i - 1) / 2][(j - 1) / 2]);
}
